def unicorn_code_hook(uc: unicorn.Uc, address: int, size: int, user_data: int) -> None:
    if address in IMPORT_LIST:
        name = IMPORT_LIST[address]
        print(f'[import] {hex(address)}: {name}')
        if utils_simAPI.simulate_import(uc, name):
            return
        raise RuntimeError(f"Failed to simulate import: {name} at {hex(address)}")
    
    d: capstone.CsInsn = list(cs.disasm(uc.mem_read(address, size), address))[0]

    print(f'{GetSegmentNameByAddr(address, user_data)}:0x{address:x}:{GetCodeTag(d)}{d.mnemonic}\t{d.op_str}')
    AsCode(address, d.size)

